class VillageMap {
    constructor() {
        this.map = map
        this.tiles = [];
        this.width = 100;
        this.height = 100;
        this.tileSize = 40

    }

    render(ctx, cameraX, cameraY) {
        const startCol = Math.floor(cameraX / this.tileSize);
        const endCol = startCol + Math.floor(this.viewportWidth / this.tileSize);
        const startRow = Math.floor(cameraY / this.tileSize);
        const endRow = startRow + Math.floor(this.viewportHeight / this.tileSize);

        for (let y = startRow; y <= endRow; y++) {
            for (let x = startCol; x <= endCol; x++) {
                const tile = this.tiles[y * this.width + x];
                if (tile) {
                    tile.render(ctx, x * this.tileSize - cameraX, y * this.tileSize - cameraY);
                }
            }
        }
    }
}

export default VillageMap